import streamlit as st

st.title("Introduction à l'Intelligence Artificielle")

st.markdown("""
    L'intelligence artificielle (IA) est un domaine vaste de l'informatique et des mathématiques. Elle est conçue pour effectuer des tâches habituellement associées à l'intelligence humaine, telles que l'apprentissage, le raisonnement, la résolution de problèmes, la perception et la prise de décision.

    On distingue deux grandes familles d'intelligence artificielle : les IA symboliques et les IA statistiques.
    
    ## Les IA symboliques
    
    L'intelligence artificielle symbolique repose sur la manipulation de symboles et de règles logiques. Elle utilise des modèles explicites de connaissances et des techniques de raisonnement pour résoudre des problèmes. Ce type d'IA est particulièrement adapté aux systèmes experts, à la planification et à la modélisation des connaissances. Un exemple courant est le moteur de règles utilisé dans les systèmes de diagnostic médical ou les assistants virtuels basés sur des arbres de décision.
    
    ## Les IA statistiques
    
    L'intelligence artificielle statistique repose sur l'analyse de grandes quantités de données pour apprendre à résoudre des problèmes. Cette approche inclut plusieurs sous-catégories d'apprentissage machine (machine learning), telles que :

    - L'apprentissage supervisé : le modèle apprend à partir d'exemples étiquetés. Exemple : la classification d'e-mails en "spam" ou "non-spam".

    - L'apprentissage non supervisé : le modèle détecte des structures dans les données sans indication préalable. Exemple : le regroupement de clients selon leurs habitudes d'achat.

    - L'apprentissage par renforcement : le modèle apprend par essais et erreurs en maximisant une récompense. Exemple : les agents utilisés dans les jeux vidéo ou la robotique.

    Ces techniques sont à la base des systèmes modernes comme la reconnaissance d'image, le traitement automatique du langage naturel (NLP) et les assistants virtuels.
    
    ## Les IA hybrides
    
    Les IA hybrides combinent les approches symboliques et statistiques afin de tirer parti des avantages de chacune. Par exemple, un assistant vocal peut utiliser une IA symbolique pour comprendre la structure d'une phrase et une IA statistique pour reconnaître la voix et interpréter les intentions de l'utilisateur. Ces systèmes sont particulièrement efficaces pour les tâches complexes nécessitant à la fois des connaissances formalisées et un apprentissage à partir de données.
""")